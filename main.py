# Создание множества со случайными элементами
моё_множество1 = {1, 2, 3, 'a', 'b', 'c'}
моё_множество2 = {3, 4, 5, 'b', 'c', 'd'}

# Вывод первого множества
print("Первое множество:", моё_множество1)

# Создание кортежа из пересечения множеств
кортеж_пересечения = tuple(моё_множество1.intersection(моё_множество2))
print("Кортеж из пересечения множеств:", кортеж_пересечения)

# Создание кортежа из разности множеств
кортеж_разности = tuple(моё_множество1.difference(моё_множество2))
print("Кортеж из разности множеств:", кортеж_разности)

# Срез первых 3 символов из кортежа
срезанный_кортеж = кортеж_пересечения[:3]
print("Срез первых 3 символов из кортежа:", срезанный_кортеж)

# Вывод символов с числами из второго множества
символы_с_числами = {элемент for элемент in моё_множество2 if isinstance(элемент, int)}
print("Символы с числами из второго множества:", символы_с_числами)

# Развертывание кортежа с помощью среза
развернутый_кортеж = кортеж_разности[::-1]
print("Развернутый кортеж:", развернутый_кортеж)

# Преобразование кортежей в списки
список_пересечения = list(кортеж_пересечения)
список_разности = list(кортеж_разности)

print("Список из кортежа пересечения:", список_пересечения)
print("Список из кортежа разности:", список_разности)

